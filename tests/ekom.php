Bootstrap adalah framework CSS yang digunakan untuk membuat tampilan website yang responsif dan menarik dengan cepat. 

Fitur Utama Bootstrap:
Grid System – Sistem grid 12 kolom yang fleksibel untuk tata letak responsif.
Komponen UI Siap Pakai – Termasuk tombol, kartu, modal, navbar, form, dan lainnya.
Responsif – Secara otomatis menyesuaikan tampilan untuk berbagai ukuran layar (mobile-friendly).
Kustomisasi Mudah – Bisa diubah sesuai kebutuhan dengan variabel SCSS.
Integrasi JavaScript – Menyediakan komponen interaktif seperti carousel, modal, dropdown, dll.
🔹 Kelebihan Bootstrap:
✅ Mudah Digunakan – Cukup tambahkan file CSS & JS, langsung bisa dipakai.
✅ Cepat & Efisien – Tidak perlu menulis banyak kode CSS dari nol.
✅ Cross-Browser – Mendukung berbagai browser modern.
✅ Komunitas Besar – Banyak dokumentasi dan tutorial yang tersedia.

