HTML (HyperText Markup Language) adalah bahasa markup yang digunakan untuk membuat dan mengstrukturkan konten di halaman web. HTML menyediakan struktur dasar yang diperlukan untuk menampilkan teks, gambar, video, dan elemen lainnya dalam browser.

HTML terdiri dari berbagai elemen (atau tag), yang digunakan untuk memberi tahu browser bagaimana menampilkan konten. Elemen-elemen ini biasanya terdiri dari tag pembuka, konten, dan tag penutup. Contoh elemen HTML yang sering digunakan adalah:

<h1> hingga <h6>: untuk membuat judul dengan berbagai tingkat (h1 adalah yang paling besar).
<p>: untuk membuat paragraf.
<a>: untuk membuat link (tautan).
<img>: untuk menampilkan gambar.
<ul> dan <ol>: untuk membuat daftar tidak terurut dan terurut.
<div>: untuk membungkus elemen lain dalam grup atau wadah.p

Laravel adalah framework PHP yang open-source dan dirancang untuk pengembangan aplikasi web yang lebih cepat dan efisien.

fitur utama laaravel
Eloquent ORM – Sistem ORM yang kuat untuk bekerja dengan database menggunakan sintaks yang sederhana dan ekspresif.
Routing yang Fleksibel – Pengelolaan rute yang mudah dengan pendekatan clean dan readable.
Blade Templating Engine – Sistem template ringan dan powerful untuk membuat tampilan dinamis.
Migration & Seeder – Memudahkan pengelolaan database tanpa harus menulis SQL langsung.
Middleware & Authentication – Sistem keamanan yang sudah built-in, termasuk autentikasi dan otorisasi.
Queue & Job System – Memproses tugas secara asinkron untuk meningkatkan performa aplikasi.
Testing & Debugging – Didukung PHPUnit untuk pengujian yang mudah.

Kelebihan Laravel:
✅ Syntax yang Elegan – Mudah dipahami dan ditulis.
✅ Komunitas Besar – Banyak dokumentasi dan dukungan dari pengembang lain.
✅ Performa Tinggi – Bisa dioptimalkan dengan caching, queue, dan sistem event.
✅ Keamanan Tinggi – Perlindungan dari SQL Injection, CSRF, dan XSS.

